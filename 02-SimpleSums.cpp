#include <iostream>  // Includes the input-output stream library for using cout
using namespace std; // Allows use of standard names like cout without 'std::' prefix

int main() {
    int num1 = 8;     // Declare an integer variable 'num1' and assign it the value 8

    int num2 = 10;    // Declare another integer variable 'num2' and assign it the value 10

    int sum = num1 + num2; // Calculate the sum of num1 and num2 and store it in 'sum'

    cout << "The sum is: " << sum << endl; // Display the result of the sum on the console

    return 0; // Return 0 to indicate the program ended successfully
}